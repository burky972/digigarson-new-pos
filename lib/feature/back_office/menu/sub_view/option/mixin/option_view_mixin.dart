        text: selectedOptionData[selectedOptionIndex ?? 0]['hPrice'].toString());
    _deliveryPriceController = TextEditingController(
        text: selectedOptionData[selectedOptionIndex ?? 0]['dPrice'].toString());
    _takeOutPriceController = TextEditingController(
        text: selectedOptionData[selectedOptionIndex ?? 0]['tPrice'].toString());
    _taxController =
        TextEditingController(text: selectedOptionData[selectedOptionIndex ?? 0]['tax'].toString());
  }

  void onRowTap(index) {
    setState(() {
      if (selectedOptionIndex == index) {
        return;
      } else {
        selectedOptionIndex = index;
        setTextFieldControllerText(index);
      }
    });
  }

  void onGroupTapped(index) {
    setState(() {
      if (selectedGroupIndex == index) {
        return;
      } else {
        selectedGroupIndex = index;
        selectedOptionIndex = 0;
        setTextFieldControllerText(0);
      }
    });
  }

  void onTaxCheckboxChanged(String key, bool value) {
    setState(() {
      if (selectedOptionIndex != null) {
        selectedOptionData[selectedOptionIndex!][key] = value;
      }
    });
  }


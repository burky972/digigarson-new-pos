        text: selectedOptionData[selectedOptionIndex ?? 0]['optionName'] ?? '');
    _optionNameController = TextEditingController(
        text: selectedOptionData[selectedOptionIndex ?? 0]['optionName'] ?? '');
    _descriptionController = TextEditingController(
        text: selectedOptionData[selectedOptionIndex ?? 0]['optionDescription'] ?? '');
